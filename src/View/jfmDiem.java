    public void loadDiem() {
    loadTable.loadData(sql, jtbDiem);
    
    HashSet<String> uniqueStudents = new HashSet<>();
    for (int i = 0; i < jtbDiem.getRowCount(); i++) {
        String maSV = jtbDiem.getValueAt(i, 0).toString(); // Assuming MaSV is in the first column
        uniqueStudents.add(maSV);
    }
    
    this.lblTongDiem.setText("Tổng số sinh viên có điểm: " + uniqueStudents.size());
}
        
    public void loadcbMaSV() {
    String sql = "select distinct MaSV from lopHoc";
    try {
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();
        while (rs.next()) {
            this.cbMaSV.addItem(rs.getString("MaSV"));
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e, "Thông Báo Lỗi", JOptionPane.ERROR_MESSAGE);
    }
}

    public void loadcbMaMH(String maSV) {
    String sql = "select distinct MaMH from lopHoc where MaSV = ?";
    try {
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, maSV);
        ResultSet rs = pst.executeQuery();
        while (rs.next()) {
            this.cbMaMH.addItem(rs.getString("MaMH"));
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e, "Thông Báo Lỗi", JOptionPane.ERROR_MESSAGE);
    }
}
