    private void jtbDiemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbDiemMouseClicked
        try
        {
            int row = this.jtbDiem.getSelectedRow(); // lấy dòng tại con trỏ chuột
            String IDrow = (this.jtbDiem.getModel().getValueAt(row, 0)).toString(); // Lấy giá trị tại con trỏ chuột theo kiểu string
            String IDrow1 = (this.jtbDiem.getModel().getValueAt(row, 1)).toString();
            String sql1 = "select * from DIEM where MaSV='"+IDrow+"' and MaMH='"+IDrow1+"'";
            ResultSet rs = loadTable.showTextField(sql1);
            // đọc dữ liệu tại dòng "IDrow"
            if(rs.next()) // nếu có dữ liệu
            {
                diem= rs.getString("MaSV");
                diem1= rs.getString("MaMH");
                this.cbMaSV.setSelectedItem(rs.getString("MaSV"));
                this.cbMaMH.setSelectedItem(rs.getString("MaMH"));
                this.txtDiemCC.setText(rs.getString("DiemCC"));
                this.txtDiemTX.setText(rs.getString("DiemTX"));
                this.txtDiemGK.setText(rs.getString("DiemGK"));
                this.txtDiemThi.setText(rs.getString("DiemThi"));
                
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }                                       

    }//GEN-LAST:event_jtbDiemMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
        // Kiểm tra các trường điểm có rỗng hay không
        if (this.txtDiemCC.getText().length() == 0 || this.txtDiemTX.getText().length() == 0 || 
            this.txtDiemGK.getText().length() == 0 || this.txtDiemThi.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin", "Thông Báo", JOptionPane.WARNING_MESSAGE);
        } else {
            // Lấy giá trị từ các trường điểm
            double diemCC = Double.parseDouble(this.txtDiemCC.getText());
            double diemTX = Double.parseDouble(this.txtDiemTX.getText());
            double diemGK = Double.parseDouble(this.txtDiemGK.getText());
            double diemThi = Double.parseDouble(this.txtDiemThi.getText());

            // Kiểm tra giá trị điểm có nằm trong khoảng từ 0 đến 10 hay không
            if (diemCC < 0 || diemCC > 10 || diemTX < 0 || diemTX > 10 || 
                diemGK < 0 || diemGK > 10 || diemThi < 0 || diemThi > 10) {
                JOptionPane.showMessageDialog(null, "Vui lòng nhập điểm trong khoảng từ 0 đến 10", "Thông Báo", JOptionPane.ERROR_MESSAGE);
            } else {
                // Nếu tất cả các điểm đều hợp lệ, thêm điểm cho sinh viên
                Diem.themDiemSV(this.cbMaSV.getSelectedItem().toString(), this.cbMaMH.getSelectedItem().toString(), 
                                this.txtDiemCC.getText(), this.txtDiemTX.getText(), this.txtDiemGK.getText(), this.txtDiemThi.getText());
                loadDiem();
            }
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Vui lòng nhập giá trị điểm hợp lệ", "Thông Báo", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnThemActionPerformed

