    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // yêu cầu nhập đầy đủ thông tin
        if ((this.txtId.getText().length()==0) || (String.valueOf(this.txtMk.getParent()).length()==0))
        {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập ID hoặc Mật khẩu","Thông báo",1);
        }
        else
        {
            rs= testConnectBLL.resul(this.txtId.getText(), String.valueOf(this.txtMk.getPassword()));
          
            try
            {
                if(rs.next())
                {
                    // đăng nhập thành công
                    tenDN=this.txtId.getText();
                    jfmMain main = new jfmMain();
                    main.setLocationRelativeTo(null);
                    this.setVisible(false);
                    main.setVisible(true);
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Tài khoản không tồn tại","Thông báo",0);
                }
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e,"Thông báo",1);
            }
            
        }
    }//GEN-LAST:event_btnLoginActionPerformed

