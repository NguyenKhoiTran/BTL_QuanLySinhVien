    private void hienThiMatKhau() {
        if (jButton2.isSelected()) {
            if(jButton2.getText().equals("Show")){//
            txtMk.setEchoChar((char) 0);
            jButton2.setText("Hide");
            } else if(jButton2.getText().equals("Hide")) {     
                //ẩn mật khẩu
                txtMk.setEchoChar('\u25cf');
                jButton2.setText("Show");          
        }
        }
    }
    
